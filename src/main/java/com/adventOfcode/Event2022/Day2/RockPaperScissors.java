    public static long calculateGame(List<List<String>> input) {

        int totalScore = 0;
        int rock = 1;
        int paper = 2;
        int scissors = 3;
        int win = 6;
        int lose = 0;
        int draw = 3;
        for (List<String> game : input) {
            String aPlayer = game.get(0).trim();
            String bPlayer = game.get(1).trim();

            // Second Part
            if ("X".equalsIgnoreCase(bPlayer)) {
                //Lose
                if ("A".equalsIgnoreCase(aPlayer)) {
                    bPlayer = "Z";
                } else if ("B".equalsIgnoreCase(aPlayer)) {
                    bPlayer = "X";
                } else {
                    bPlayer = "Y";
                }
            } else if ("Y".equalsIgnoreCase(bPlayer)) {
                //Draw
                if ("A".equalsIgnoreCase(aPlayer)) {
                    bPlayer = "X";
                } else if ("B".equalsIgnoreCase(aPlayer)) {
                    bPlayer = "Y";
                } else {
                    bPlayer = "Z";
                }
            } else {
                //Win
                if ("A".equalsIgnoreCase(aPlayer)) {
                    bPlayer = "Y";
                } else if ("B".equalsIgnoreCase(aPlayer)) {
                    bPlayer = "Z";
                } else {
                    bPlayer = "X";
                }
            }

            if ("A".equalsIgnoreCase(aPlayer)) {
                if ("Y".equalsIgnoreCase(bPlayer)) {
                    totalScore = totalScore + (paper + win);
                } else if ("Z".equalsIgnoreCase(bPlayer)) {
                    totalScore = totalScore + (scissors + lose);
                } else {
                    totalScore = totalScore + (rock + draw);
                }
            } else if ("B".equalsIgnoreCase(aPlayer)) {
                if ("Y".equalsIgnoreCase(bPlayer)) {
                    totalScore = totalScore + (paper + draw);
                } else if ("Z".equalsIgnoreCase(bPlayer)) {
                    totalScore = totalScore + (scissors + win);
                } else {
                    totalScore = totalScore + (rock + lose);
                }
            } else if ("C".equalsIgnoreCase(aPlayer)) {
                if ("Y".equalsIgnoreCase(bPlayer)) {
                    totalScore = totalScore + (paper + lose);
                } else if ("Z".equalsIgnoreCase(bPlayer)) {
                    totalScore = totalScore + (scissors + draw);
                } else {
                    totalScore = totalScore + (rock + win);
                }
            }
        }
        return totalScore;
    }
